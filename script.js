let numero = parseFloat(prompt('digite um numero 1'));
let numero2 = parseFloat(prompt('digite um numero 2'));

let operaçao = prompt("digite '+' para adiçao e '-' para subtraçao");

let=resultado;

if (operaçao ==="-"){
    resultado = numero - numero2
    alert("resultado da subtraçao"+ resultado)
}
else {
    alert("operaçao invalida");
}


if (operaçao ==="+") {
    resultado = numero + numero2
    alert("resultado da soma" - resultado);
}

else {
    alert("operaçao invalida");
}